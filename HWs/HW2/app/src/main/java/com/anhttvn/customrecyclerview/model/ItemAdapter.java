package com.anhttvn.customrecyclerview.model;

public class ItemAdapter {
    private int image;
    private String text;

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getText() {
        return text;
    }
}
